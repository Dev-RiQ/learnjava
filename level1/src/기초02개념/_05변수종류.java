package 기초02개념;

public class _05변수종류 {

	public static void main(String[] args) {

		// 자료형 : 숫자 문자 논리
		// 정수
		int a = 10;
		long b = 1000000000000L;
		System.out.println(a);
		System.out.println(b);
		//실수
		double c = 123.123;
		float d = 123.123f;
		System.out.println(c);
		System.out.println(d);
		
	}

}