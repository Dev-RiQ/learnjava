package 기초01개념;

public class _03출력문 {
	
	public static void main(String[] args) {
		/*
		 * [문제]
		 * 		다음과 같이 콜솔 창에 출력해보세요.
		 * 		[1] "안녕하세요"
		 * 		[2] '안녕하세요'
		 * 		[3] 안녕
		 * 			하세요
		 * 		[4] /\안녕하세요/\
		 */
		
		System.out.println("\"안녕하세요\"");
		System.out.println("'안녕하세요'");
		System.out.println("안녕\n하세요"); //엔터
		System.out.println("/\\안녕하세요/\\");
		
	}

}
