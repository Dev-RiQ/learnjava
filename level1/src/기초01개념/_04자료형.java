package 기초01개념;

// 자바스크립트 : 자료형 number(정수, 실수), string(문자), undefind(초기값이 없는 데이터)

// 자료형 : data type(자료의 형태) -> 컴퓨터가 인식/구별할 수 있는 데이터 형식 구분 

/*
	자료형 종류
	숫자 : 정수int, 실수double
	문자 : 한개 char(' '), 여러개 String(" ")
	논리 : boolean 값이 딱 두개 (참 / 거짓)
 */
public class _04자료형 {
	
	public static void main(String[] args) {
		
		System.out.println(10 );
		System.out.println(10.0);
		System.out.println('가');
		System.out.println("가나다");
		System.out.println(true);
		System.out.println(false);
		
	}

}
